function theBeatlesPlay(musicians,instruments) {
  var m = [];
  for(let i = 0; i < m.length - 1; i++) {
    m.push(`${musicians[i]} plays ${instruments[i]}`);
  }
return m
}
function johnLennonFacts(facts) {
  var f = [];
  while(facts) {
    f.push(`${facts} + "!!!"`)
  }
  return f
}


















